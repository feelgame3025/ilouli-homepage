export const checkEnglishExpression = (prompt) => {
  const englishPatterns = [
    /I\s+\w+/i,
    /Let's\s+\w+/i,
    /Look\s+at/i,
    /This\s+is/i,
    /Thank\s+you/i
  ];

  return englishPatterns.some(pattern => pattern.test(prompt));
};

